<p>Denying oneself food in consecration to God is one of the highest forms and acts of worship. Muslims have a month in a year that is dedicated to this holy act. They fast from dawn to dusk daily.

<p>Christians too fast even though their fasting is not structured. It depends on churches and individuals.

<p>Some churches call for 40 days of prayer and fast at the beginning of the year.                    <div class="article-adv">    
 
