<p>Whatever the case, fasting is recognised not only for its spiritual benefits but for the physical benefits as well.

<p>The chief purpose of fasting is to strengthen us in times of trial, sorrow and adversity.

<p>There are numerous examples in the Scriptures of people who fasted during difficult times. They include Jesus, Zachaeus, Cornelius, Barnabas and Saul.

<p>When properly engaged in, fasting can be a source of great spiritual blessing, a discipline of the will and an exercise producing inward strength, and power.

<p>There are other reasons and benefits for which we engage in fasting:

<p>&bull; Fasting helps subject our bodies to our spirit. The flesh is presented as very dangerous if it operates without the spirit. It is through the weakness of the flesh that people fall into various sins such as immorality, lust and greed. When we fast, we subordinate our flesh-desires to our spirit-desires. 

