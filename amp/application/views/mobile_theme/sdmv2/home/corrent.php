<p>The local government will lay off hundreds of casual workers at the end of this month in a bid to reduce the soaring wage bill.

<p>&quot;Following the management meeting held on November 3, it was resolved that the contracts entered into with officers on casual and temporary terms be terminated,&quot; a letter signed by County Human Resource Director Jonathan Kipchoge partly reads.                    <div class="article-adv">     
  
</div></div>
                    

<p>&nbsp;Mr Kipchoge noted that the Vihiga county administration issued a 21-day notice of intention to terminate the contracts.

<p>Vihiga county pays slightly over Sh100 million in salaries every month with concerns that the wage bill was increasing every month.

<p>The move to retrench the casuals and other temporary staff would see more than 400 workers rendered jobless. The county government has set aside Sh8 million for the exercise. Every affected staff will pocket Sh21,000 as a send-off package.                    <div class="article-adv">    
  
</div></div>
                    

<p>The letter sent to all 10 chief officers and copied to Governor Moses Akaranga, County Secretary Linet Abdallah, all county executives and the county public service board, advised the chief officers to inform all the affected workers in their respective departments.<div class="mid-recommend">
See also: <a href="http://www.standardmedia.co.ke/mobile/article/2000180720/contractors-in-vihiga-demand-payment">Contractors in Vihiga demand payment</a>
</div>


<p>&quot;It is anticipated that by November 30, all such contracts will cease,&quot; the letter reads in part.

<p>Chief officers who feel that the retrenchment exercise would affect critical areas in their departments were advised to raise the matter with the public service board.

<p>Those to be axed include market cleaners, revenue collectors, enforcement officers, tea girls, ward staff, clerical officers, drivers, technicians, office assistants and grounds men. Various departments have already informed their casuals about the process. A letter addressed to the affected staff, and signed by John Khamala on behalf of the Luanda town manager communicated the retrenchment message.

<p>The Commission for Revenue Allocation (CRA) has allocated the county Sh3.8 billion this financial year. The county&#39;s debt was more than Sh1 billion in the last financial year and its local revenue dipped from Sh300 million to Sh80 million annually.
 

